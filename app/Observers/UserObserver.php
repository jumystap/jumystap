    /**
     * Handle the User "updated" event.
     */
    public function updated(User $user): void
    {
        if($user->isDirty('is_blocked')){
            $user->announcement()
                ->where('status', AnnouncementStatus::ACTIVE->value)
                ->update(['status' => AnnouncementStatus::ARCHIVED->value]);
        }
    }

